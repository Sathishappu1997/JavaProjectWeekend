package day02;

public class DataTypes {
	
	public static void main(String[] args) {
		
		//Primitive Datatypes
		//Numeric
		byte a = 127;	//127
		short b = 12346;
		int c = 345678945;
		long d = 2345678934567890789l;
		
		//Decimal
		float e = 34.67f;
		double f = 423456567.345678902345;
		
		boolean g = false;
		char h = 'A';
		
		//Non-Primtive Datatypes
		//String, Array, etc
		String i = "Arjun";
		
		System.out.println("The Byte value is "+a);
		System.out.println("The Short value is "+b);
		System.out.println("The Int value is "+c);
		System.out.println("The Long value is "+d);
		System.out.println("The Float value is "+e);
		System.out.println("The Double value is "+f);
		System.out.println("The Boolean value is "+g);
		System.out.println("The Character value is "+h);
		System.out.println("The String value is "+i);
		System.out.println(a+i);
		
	}

}
